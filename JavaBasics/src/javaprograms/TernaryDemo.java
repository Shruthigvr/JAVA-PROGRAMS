package javaprograms;

public class TernaryDemo {

	public static void main(String[] args) {
		// variable=(condition) ? TrueExpression : FalseExpression
		
		int num1=60;
		int num2=30;
		
		String result= (num1>num2) ? "True" : "False";
		System.out.println("Result : "+result);

	}

}
